#include "glwidget.h"

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

GLWidget::GLWidget(QWidget *parent)
    : QOpenGLWidget(parent)
{
    m_xRot = 0;
    m_yRot = 0;
    m_zRot = 0;
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

GLWidget::~GLWidget()
{
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

QSize GLWidget::minimumSizeHint() const
{
    return QSize(200, 200);
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

QSize GLWidget::sizeHint() const
{
    return QSize(200, 200);
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

static void qNormalizeAngle(double &angle)
{
    while (angle < 0)
        angle += 360 * 16;
    while (angle > 360 * 16)
        angle -= 360 * 16;
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

void GLWidget::setXRotation(double angle)
{
    qNormalizeAngle(angle);
    if (angle != m_xRot) {
        m_xRot = angle;
        update();
    }
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

void GLWidget::setYRotation(double angle)
{
    qNormalizeAngle(angle);
    if (angle != m_yRot) {
        m_yRot = angle;
        update();
    }
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

void GLWidget::setZRotation(double angle)
{
    qNormalizeAngle(angle);
    if (angle != m_zRot) {
        m_zRot = angle;
        update();
    }
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

void GLCreateCube(float x, float y, float z) {

    glPushMatrix();
    glScalef(x,y,z);

    // Yellow side
    glBegin(GL_POLYGON);
    glColor3f(1.0, 1.0, 0.0);
    glVertex3f(  0.5, -0.5, -0.5);
    glVertex3f(  0.5, 0.5, -0.5);
    glVertex3f( -0.5,  0.5, -0.5);
    glVertex3f( -0.5, -0.5, -0.5);
    glEnd();

    // White side
    glBegin(GL_POLYGON);
    glColor3f(1.0, 0.0, 1.0);
    glVertex3f(  0.5, -0.5,  0.5);
    glVertex3f(  0.5,  0.5,  0.5);
    glVertex3f( -0.5,  0.5,  0.5);
    glVertex3f( -0.5, -0.5,  0.5);
    glEnd();

    // Purple side
    glBegin(GL_POLYGON);
    glColor3f(1.0, 1.0, 1.0);
    glVertex3f(  0.5, -0.5, -0.5);
    glVertex3f(  0.5,  0.5, -0.5);
    glVertex3f(  0.5,  0.5,  0.5);
    glVertex3f(  0.5, -0.5,  0.5);
    glEnd();

    // Green side
    glBegin(GL_POLYGON);
    glColor3f(0.0, 1.0, 0.0);
    glVertex3f( -0.5, -0.5,  0.5);
    glVertex3f( -0.5,  0.5,  0.5);
    glVertex3f( -0.5,  0.5, -0.5);
    glVertex3f( -0.5, -0.5, -0.5);
    glEnd();

    // Blue side
    glBegin(GL_POLYGON);
    glColor3f(0.0, 0.0, 1.0);
    glVertex3f(  0.5,  0.5,  0.5);
    glVertex3f(  0.5,  0.5, -0.5);
    glVertex3f( -0.5,  0.5, -0.5);
    glVertex3f( -0.5,  0.5,  0.5);
    glEnd();

    // Red side
    glBegin(GL_POLYGON);
    glColor3f(1.0, 0.0, 0.0);
    glVertex3f(  0.5, -0.5, -0.5);
    glVertex3f(  0.5, -0.5,  0.5);
    glVertex3f( -0.5, -0.5,  0.5);
    glVertex3f( -0.5, -0.5, -0.5);
    glEnd();

    glPopMatrix();
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

void GLWidget::initializeGL() {


    // Enable background color
    glClearColor(static_cast<GLclampf>(0.2),
                 static_cast<GLclampf>(0.2),
                 static_cast<GLclampf>(0.2),
                 1);

    // Enable depth buffer
    glEnable(GL_DEPTH_TEST);
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

void GLWidget::paintGL() {

    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

    glMatrixMode(GL_MODELVIEW);
    glLoadIdentity();
    gluLookAt(0,0,5, 0,0,0, 0,1,0);

    glRotated(m_yRot,  0,1,0);
    glRotated(m_zRot,  0,0,1);
    glRotated(m_xRot,  1,0,0);

    GLCreateCube(2.5 * 0.75, 0.5 * 0.75, 2 * 0.75);
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

void GLWidget::resizeGL(int w, int h) {

    glViewport(0,0,w,h);
    glMatrixMode(GL_PROJECTION);
    glLoadIdentity();
    gluPerspective(45, (float)w/h, 0.01, 100.0);
    glMatrixMode(GL_MODELVIEW);
    glLoadIdentity();
    gluLookAt(-0.5,0,4,0,0,-100,0,1,0);
}

